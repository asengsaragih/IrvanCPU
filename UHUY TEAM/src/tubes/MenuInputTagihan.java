    public void IsiTabel()throws SQLException{
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("idTanggungan");
        model.addColumn("bulan");
        model.addColumn("keteranganTanggungan");
        model.addColumn("nominal");
        
        try {
            Statement state = (Statement) con.createStatement();
            String sql = "SELECT * FROM tanggungan";
            ResultSet rs = state.executeQuery(sql);
            while (rs.next()) {            
                model.addRow(new Object[]{rs.getInt("idTanggungan"), rs.getInt("bulan"), rs.getString("keteranganTanggungan"), rs.getInt("nominal")});
            }
            tblTagihan.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

