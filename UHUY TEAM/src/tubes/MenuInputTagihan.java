    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Statement st = (Statement) con.createStatement();
            String sql = "INSERT INTO tanggungan VALUES ("+txtId.getText()+","+txtBulan.getText()+",'"+txtKeterangan.getText()+"',"+txtNominal.getText()+")";
            st.execute(sql);
            JOptionPane.showMessageDialog(rootPane, "Data Berhasil Terkirim");
            
            String sqlsiswa = "SELECT * FROM siswa";
            ResultSet rs = st.executeQuery(sqlsiswa);

            int hit = 0;
            while (rs.next()) {                
                arr_nis.add(rs.getInt("nis"));
                arr_tagihan.add(rs.getInt("Tanggungan"));
                hit++;
            }
           
               
            for (int i = 0; i < hit; i++) {
                int baru = 0;
                baru = Integer.parseInt(txtNominal.getText());
                int lama = (int) arr_tagihan.get(i);
                baru = baru+lama;
                //String sql2 = "UPDATE siswa SET Tanggungan = "+(txtNominal.getText()+arr_tagihan.get(i))+" WHERE nis ="+arr_nis.get(i);
                String sql2 = "UPDATE siswa SET Tanggungan = "+baru+" WHERE nis ="+arr_nis.get(i);
                
                st.execute(sql2);
            }
            
            IsiTabel();
            Blank();
        
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage()+"Erorrrrrrrrrr");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

