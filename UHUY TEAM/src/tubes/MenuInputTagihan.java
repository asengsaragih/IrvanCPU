    private void tblTagihanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTagihanMouseClicked
        try {
        int i = tblTagihan.getSelectedRow();
        TableModel model = tblTagihan.getModel();
        int id = (int) model.getValueAt(i, 0);
        int nominal = (int) model.getValueAt(i, 3);
        txtId.setText(Integer.toString(id));
        txtBulan.setText(model.getValueAt(i, 1).toString());
        txtKeterangan.setText(model.getValueAt(i, 2).toString());
        txtNominal.setText(Integer.toString(nominal));
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_tblTagihanMouseClicked

