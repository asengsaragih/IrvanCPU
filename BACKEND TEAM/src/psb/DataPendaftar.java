    private void load_data(){
         db.dbConnection();
        try {
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("NISN");
            model.addColumn("Nama");
            model.addColumn("Nilai UN");
        try {
            String sql = "SELECT S.nisn, S.nama_lengkap, N.jumlah_nilai FROM siswa_baru S, siswa_baru_nilai N WHERE S.nisn = N.nisn";
            Statement st = db.getConnection().createStatement();
            ResultSet rsView = st.executeQuery(sql);
            while (rsView.next()) {
                model.addRow(new Object[]{rsView.getString("S.nisn"),rsView.getString("S.nama_lengkap"),rsView.getString("N.jumlah_nilai")});
            } jTable1.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
