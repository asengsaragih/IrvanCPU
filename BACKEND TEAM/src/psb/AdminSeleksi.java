    private void loadSiswaIps(){
         db.dbConnection();
        try {
            DefaultTableModel model = new DefaultTableModel();
            
            model.addColumn("NISN");
            model.addColumn("Nama");
            
            model.addColumn("Bahasa");
            model.addColumn("Inggris");
            model.addColumn("matematika");
            model.addColumn("Ipa");
            model.addColumn("Jumlah");
            
            String sql = "SELECT s.nisn, s.nama_lengkap, n.nilai_bahasa, n.nilai_inggris, n.nilai_matematika, n.nilai_ipa, n.jumlah_nilai \n" +
"FROM siswa_baru s, siswa_baru_nilai n, nilai_minimal_ips m, user u\n" +
"WHERE n.nilai_bahasa >= m.bahasa AND n.nilai_inggris >= m.inggris AND n.nilai_matematika >= m.matematika AND n.nilai_ipa >= m.ipa AND \n" +
"n.peminatan_1 = 'IPS'\n AND s.status != 'lulus'"  +
"AND s.nisn = n.nisn AND u.id_user = m.id_user order by n.jumlah_nilai asc;";
            Statement st = db.getConnection().createStatement();
            ResultSet rsView = st.executeQuery(sql);
            while (rsView.next()) {
                model.addRow(new Object[]{rsView.getString("s.nisn"),rsView.getString("S.nama_lengkap"),rsView.getString("n.nilai_bahasa"),rsView.getString("n.nilai_inggris"),rsView.getString("n.nilai_matematika"),rsView.getString("n.nilai_ipa"),rsView.getString("n.jumlah_nilai")});
            } tablesiswaips.setModel(model);
            
               
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
