    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        if (repassword.getText().equals(password.getText())) {
            try {
                String idPegawai = idpegawai.getText();
                String pass = password.getText();
                String tipee = role.getSelectedItem().toString();
                String[] tipe = tipee.split("-");
                int rol = Integer.parseInt(tipe[0]);
                String id = new IdGenerator("USR","user").generate();
                System.out.println(id);

                Statement stmt = db.createStatement();
                stmt.execute("INSERT INTO user VALUES('"+id+"','"+idPegawai+"','"+pass+"',"+rol+")");
                JOptionPane.showMessageDialog(this, "Pendaftaran Berhasil!");
                Cookies.create(id, pass, rol);
                this.dispose();
                if (rol == 2) {
                    this.dispose();
                    new Kasir().setVisible(true);
                } else {
                    this.dispose();
                    new Koki().setVisible(true);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Terjadi Kesalahan!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Password tidak sama!");
            repassword.setText("");
        }
    }//GEN-LAST:event_jLabel2MouseClicked

