    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        if (selectedRowTable >= 0) {
            try {
                String idVoucher = tabelVoucher.getValueAt(selectedRowTable, 0).toString();
                Statement stmt = db.createStatement();
                stmt.execute("DELETE FROM voucher WHERE id_voucher='"+idVoucher+"'");
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                getData();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Terjadi Kesalahan : "+e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Silahkan pilih tabel dulu");
        }
    }//GEN-LAST:event_deleteMouseClicked

