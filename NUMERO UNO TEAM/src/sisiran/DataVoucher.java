    private void getData() {
        try {
            DefaultTableModel model = new DefaultTableModel();
            
            model.addColumn("Kode Voucher");
            model.addColumn("Stok");
            model.addColumn("Potongan");
            Statement stmt = db.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM voucher ORDER BY id_voucher ASC");
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("id_voucher"),rs.getString("stok_voucher"),rs.getString("potongan")});
            }
            tabelVoucher.setModel(model);
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

