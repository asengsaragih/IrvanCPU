    private void loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginMouseClicked
        String username = idpegawai.getText();
        String pass = password.getText();
        
        try {
            Statement stmt = db.createStatement();
            String query = "SELECT * FROM user WHERE username='"+username+"' AND password='"+pass+"'";
            ResultSet rs = stmt.executeQuery(query);
            rs.next();
            int role = rs.getInt("role");
            if (role == 1) {
                Cookies.create(rs.getString("id_user"), username, rs.getInt("role"));
                this.dispose();
                new HeadOffice().setVisible(true);
                JOptionPane.showMessageDialog(this, "Anda Berhasil Login");
            } else if (role == 2) {
                Cookies.create(rs.getString("id_user"), username, rs.getInt("role"));
                this.dispose();
                new Kasir().setVisible(true);
                JOptionPane.showMessageDialog(this, "Anda Berhasil Login");
            } else if (role == 3) {
                Cookies.create(rs.getString("id_user"), username, rs.getInt("role"));
                this.dispose();
                new Koki().setVisible(true);
                JOptionPane.showMessageDialog(this, "Anda Berhasil Login");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Username / Password salah");
            idpegawai.setText("");
            password.setText("");
        }
    }//GEN-LAST:event_loginMouseClicked

