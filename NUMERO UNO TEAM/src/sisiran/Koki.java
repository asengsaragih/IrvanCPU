    private void getData() {
        try {
            DefaultTableModel model = new DefaultTableModel();
            
            model.addColumn("Kode Voucher");
            model.addColumn("Stok");
            model.addColumn("Potongan");
            Statement stmt = db.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT transaksi.id_transaksi, menu.nama, transaksi.quantity "
                    + "FROM transaksi, menu WHERE transaksi.id_menu = menu.id_menu AND "
                    + "status = 1 ORDER BY transaksi.id_transaksi ASC");
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("id_transaksi"),rs.getString("nama"),rs.getInt("quantity")});
            }
            tabelPesanan.setModel(model);
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

