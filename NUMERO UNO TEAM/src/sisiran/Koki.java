    private void selesaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_selesaiMouseClicked
        if (selectedRowTable >= 0) {
            try {
                String idTransaksi = tabelPesanan.getValueAt(selectedRowTable, 0).toString();
                Statement stmt = db.createStatement();
                stmt.execute("UPDATE transaksi SET status=2 WHERE id_transaksi='"+idTransaksi+"'");
                JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                getData();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Terjadi Kesalahan : "+e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Silahkan pilih tabel dulu");
        }
    }//GEN-LAST:event_selesaiMouseClicked

