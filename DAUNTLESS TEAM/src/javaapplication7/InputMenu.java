    private void inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputActionPerformed
        // TODO add your handling code here:
        try {
            String Kategori = kategori.getSelectedItem().toString();
            String Nama = nama.getText();
            String Harga = harga.getText();
            String desc = deskripsi.getText();
            
            Statement stmt = db.createStatement();
            
            stmt.execute("INSERT INTO inputmenu (kategori,nama,harga,deskripsi)"
                    + "VALUES('"+Kategori+"','"+Nama+"','"+Harga+"','"+desc+"')");
            
            JOptionPane.showMessageDialog(this, "Input Menu Berhasil!"); 
            clearForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_inputActionPerformed

