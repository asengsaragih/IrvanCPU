    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = user.getText();
        String password = pass.getText();
        try {
            Statement stmt = db.createStatement();
            String query = "SELECT * FROM user WHERE username='"+username+"' AND password='"+password+"'";
            ResultSet rs = stmt.executeQuery(query);
            rs.next();
            if (rs.getRow()>0) {
                Cookies.create(rs.getInt("idUser"), username, rs.getString("alamat"),"");
                this.dispose();
                new LandingPageCustomer().setVisible(true);
                JOptionPane.showMessageDialog(this, "Anda Berhasil Login");
            } else {
                Statement stmtt = db.createStatement();
                String queryy = "SELECT * FROM driver WHERE username='"+username+"' AND password='"+password+"'";
                ResultSet rss = stmtt.executeQuery(queryy);
                rss.next();
                if (rss.getRow()>0) {
                    Cookies.create(rss.getInt("idDriver"), username, "",rss.getString("notelp"));
                    this.dispose();
                    new HalamanDriver().setVisible(true);
                    JOptionPane.showMessageDialog(this, "Anda Berhasil Login");  
                }else{
                    JOptionPane.showMessageDialog(this, "Gagal Login");  
                }
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Username / Password salah "+e);
            user.setText("");
            pass.setText("");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

