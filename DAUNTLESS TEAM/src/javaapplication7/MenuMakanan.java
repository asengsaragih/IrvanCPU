    private void table1TampilData(String table){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Nama");
        model.addColumn("Harga");
        
        try {
            Statement stmt = db.createStatement();
             ResultSet res = stmt.executeQuery("select * from inputmenu where kategori='"+table+"' ");

            while(res.next()){
                model.addRow(new Object[]{res.getString("idMenu"),res.getString("nama"),res.getString("harga")});
            }table1.setModel(model);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
    private void table2TampilData(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Nama");
        model.addColumn("Banyak");
        model.addColumn("Harga");
        model.addColumn("Total");
        int idUser = Cookies.getIdUser();
        
        try {
            Statement stmt = db.createStatement();
             ResultSet res = stmt.executeQuery("select transaksi.idtrx, inputmenu.nama, "
                     + "transaksi.banyak, inputmenu.harga, transaksi.subtotal from transaksi,inputmenu "
                     + "WHERE transaksi.idMenu=inputmenu.idMenu AND transaksi.idUser = "+idUser+" AND "
                     + "status=0");

            while(res.next()){
                model.addRow(new Object[]{res.getInt("idtrx"),res.getString("nama"),res.getInt("banyak")
                            ,res.getInt("harga"), res.getInt("subtotal")});
            }table2.setModel(model);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
