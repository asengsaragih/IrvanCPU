    private void tabeltampilsoal() {
         DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id_question");
        model.addColumn("Soal");
        model.addColumn("Jawaban");
        model.addColumn("Level");
        
        try {
            Statement tsql = db.createStatement();
            ResultSet res = tsql.executeQuery("select * from question order by id_question");
            while (res.next()) {
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4)});
            } tabelsoal.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
     private void tabeltampiluser() {
         DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Name");
     
        try {
            Statement tsql = db.createStatement();
            ResultSet res = tsql.executeQuery("select * from user order by id_user");
            while (res.next()) {
                model.addRow(new Object[]{res.getString(1),res.getString(2)});
            } tabeluser.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
