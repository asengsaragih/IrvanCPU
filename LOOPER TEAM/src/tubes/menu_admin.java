private void tampilDataAdmin() {
        String id_pem = user_session.getU_id();
        DefaultTableModel model2 = new DefaultTableModel();
        model2.addColumn("ID");
        model2.addColumn("Tanggal peminjaman");
        model2.addColumn("Waktu mulai");
        model2.addColumn("Waktu berakhir");
        model2.addColumn("Tanggal pengajuan");
        model2.addColumn("ID peminjam");
        model2.addColumn("ID ruangan");
        model2.addColumn("Keperluan");
        model2.addColumn("Status");
        
        
        
        try {
            Statement tsql3 = db.createStatement();
//            String sql= ;
            ResultSet resu=tsql3.executeQuery("select * from detail_peminjaman ");
            
             while (resu.next()) {
                model2.addRow(new Object[]{resu.getString(1),resu.getString(2),resu.getString(3),resu.getString(4),resu.getString(5),resu.getString(7),resu.getString(9),resu.getString(10),resu.getString(11)});
            } tabel_admin.setModel(model2);
            
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
