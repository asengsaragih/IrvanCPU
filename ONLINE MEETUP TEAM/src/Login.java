    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        try{
            String uname = lgnuname.getText();
            String pass = new String(lgnpass.getPassword());

            Statement st = (Statement)koneksi.getConnection().createStatement();
            String sql = "select * from login where username = '"+uname+"' And password = '"+pass+"'";
            res = st.executeQuery(sql);

            if (res.next()) {
                if(lgnpass.getText().equals(res.getString("password"))){
                    new Home().show();
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Password salah, silakan coba lagi");
                    lgnpass.setText("");
                    lgnpass.requestFocus();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Login gagal");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "gagal");
        }
    }//GEN-LAST:event_loginActionPerformed

