 private void tablepinjamTampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id Peminjam");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("No telepon");
        model.addColumn("No Identitas");
        model.addColumn("Tipe Mobil");
        model.addColumn("Merk Mobil");
        model.addColumn("Harga");
        model.addColumn("Durasi");
        model.addColumn("Tanggal Pinjam");
        model.addColumn("Tanggal Kembali");
        
        try{
            Statement st = (Statement)koneksi.getConnection().createStatement();
            ResultSet res = st.executeQuery("select * from pelanggan");
            while (res.next()) {
                model.addRow(new Object[]{res.getString("id_peminjam"), res.getString("nama"),res.getString("alamat"),res.getString("no_telepon"),res.getString("no_identitas"),res.getString("tipe_mobil"), res.getString("merk_mobil"),res.getInt("harga"),res.getInt("Durasi"),res.getString("tgl_pinjam"),res.getString("tgl_kembali")});
            }tablepinjam1.setModel(model);
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }

