    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
         String namaP = namaplg.getText();
        String alamatP = alamatplg.getText();
        String notelpP = notelpplg.getText();
        String identitasP = identitasplg.getText();
        String tipeM = (String) tipembl.getSelectedItem();
        String merkM = merkmbl.getSelectedItem().toString();
        int harga = Integer.parseInt(hargambl.getText());
        int durasi = Integer.parseInt(combdurasi.getSelectedItem().toString());
        
       
        
        try {
            DateFormat dateformat = new SimpleDateFormat("yyyy-MM-dd");
            Date date = new Date();
            String tanggalTransaksi = dateformat.format(date);
            Statement st = (Statement)koneksi.getConnection().createStatement();
            st.execute("insert into pelanggan (nama, alamat, no_telepon, no_identitas, tipe_mobil, merk_mobil,harga,durasi,tgl_pinjam,tgl_kembali)values"
                + "('" + namaP + "','" +alamatP+ "','" +notelpP+ "','"+ identitasP+ "','" +tipeM+  "','" +merkM + "'," +harga + "," +durasi + ",'" +tanggalTransaksi + "',DATE_ADD(NOW(), INTERVAL "+durasi+" DAY))");
            st.execute("update tbl_mobil set status = 1 where merk = '"+merkM+"'");
            JOptionPane.showMessageDialog(this, "Data Pelanggan berhasil Ditambahkan");
            Statement stUpdate = (Statement)koneksi.getConnection().createStatement();
            
            stUpdate.execute("update tbl_mobil set status = '" + 0 + "' where merk = '" + merkmbl.getSelectedItem()+"'");
//            JOptionPane.showMessageDialog(null, "Data berhasil diupdate");

            clearPinjam();
            tablepinjamTampilData();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }//GEN-LAST:event_tambahActionPerformed

