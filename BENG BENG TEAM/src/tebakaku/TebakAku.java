    private void timerGame(){
        // Timer Soal
        counter = 180;
        TimerTask timerTask = new TimerTask() {
            @Override
            public void run() {
                textWaktu.setText(Integer.toString(counter));
                counter--;
                if(counter == -1){
                    JOptionPane.showMessageDialog(rootPane, "Waktu Habis");
                    timer.cancel();
                    new MainMenu().setVisible(true);
                    setVisible(false);
                }
            }
        };
        timer.scheduleAtFixedRate(timerTask, 1000, 1000);
    }
    private void generateSoal(){
        try{
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT * FROM quiz_tebak_aku ORDER BY RAND() LIMIT 5");
            while(resultSet.next()){
                soalQuiz.add(resultSet.getString("soal_quiz"));
                jawabanQuiz.add(resultSet.getString("jawaban_quiz"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
    private void tampilSoal(){
        try{
            String soalSekarang = this.soalQuiz.get(indexSoal);
            textSoal.setText("<html>"+soalSekarang+"</html>");
            if(indexSoal == soalQuiz.size()-1){
                btnSelesai.setVisible(true);
                btnLanjut.setVisible(false);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }
    private void btnLanjutMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLanjutMouseClicked
        // TODO add your handling code here:
        try{
            String jawabanUser = inputJawabanUser.getText();
            String jawabanBenar = jawabanQuiz.get(indexSoal);
            if(jawabanUser.equals("")){
                JOptionPane.showMessageDialog(this, "Eits, inputan kamu ga boleh kosong :)");
            }else{
                if(jawabanUser.equalsIgnoreCase(jawabanBenar)){
                    skorUser += 10;
                    indexSoal++;
                    tampilSoal();
                    inputJawabanUser.setText("");
                    textSkor.setText(""+skorUser);
                }else{
                    indexSoal++;
                    tampilSoal();
                    inputJawabanUser.setText("");
                }    
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnLanjutMouseClicked

    private void btnSelesaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSelesaiMouseClicked
        // TODO add your handling code here:
        try{
            String jawabanUser = inputJawabanUser.getText();
            String jawabanBenar = jawabanQuiz.get(indexSoal);
            if(jawabanUser.equalsIgnoreCase(jawabanBenar)){
                skorUser += 10;
            }
            if(CookiesUser.getSkorTebakAku() < skorUser){
                Statement statement = connection.createStatement();
                statement.execute("UPDATE temp_name SET skor_tebak_aku = '"+skorUser+"' WHERE nameUser = '"+CookiesUser.getNamaUser()+"'");
            }
            JOptionPane.showMessageDialog(this, "Yeahh selamat, skor kamu : "+skorUser);
            timer.cancel();
            new MainMenu().setVisible(true);
            this.setVisible(false);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }
    }//GEN-LAST:event_btnSelesaiMouseClicked

