    private void btnLanjutkanPermainanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLanjutkanPermainanMouseClicked
        // TODO add your handling code here:
        if(inputNama.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Isi nama kamu dulu yaa :)");
        }else{
            String nama = inputNama.getText();
            try{
                Statement statement = connection.createStatement();
                ResultSet resultSet = statement.executeQuery("SELECT * FROM temp_name WHERE nameUser = '"+nama+"'");
                resultSet.next();
                if(resultSet.getRow() == 1){
                    CookiesUser.setNamaUser(nama);
                    CookiesUser.setSkorTebakAku(0);
                    CookiesUser.setSkorTebakGambar(0);
                    CookiesUser.setSkorTebakAngka(0);
                    Menu menu = new Menu(nama);
                    menu.setVisible(true);
                    this.setVisible(false);
                }else{
                    JOptionPane.showMessageDialog(this, "Nama kamu belum terdaftar, mulai permainan baru dulu yaa :)");
                    new MainMenu().setVisible(true);
                    this.setVisible(false);
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e);
                System.exit(0);
            }
        }
    }//GEN-LAST:event_btnLanjutkanPermainanMouseClicked

