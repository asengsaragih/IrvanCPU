    private void tampilHighScore(String fieldColoum){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Peringkat");
        model.addColumn("Nama Pemain");
        model.addColumn("Skor");
        
        int peringkat = 1;
        try{
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT * FROM temp_name ORDER BY "+fieldColoum+" DESC LIMIT 10");
            while(resultSet.next()){
                model.addRow(new Object[]{
                    peringkat++,
                    resultSet.getString("nameUser"),
                    resultSet.getString(fieldColoum)
                });
            }
            tabelSkor.setModel(model);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
    private void tampilSkor(){
        if(sumber == "TEBAK AKU"){
            tampilHighScore("skor_tebak_aku");        
        }else
        if(sumber == "TEBAK ANGKA"){
            tampilHighScore("skor_tebak_angka");          
        }else
        if(sumber.equalsIgnoreCase("TEBAK GAMBAR")){
            tampilHighScore("skor_tebak_gambar");        
        }
    }
