        try {
            Class.forName("com.mysql.jdbc.Driver");
            String url ="jdbc:mysql://localhost/rumah_sakit";
            String user = "root";
            String pas =  "";
            Connection con = DriverManager.getConnection(url, user, pas);
            Statement stm = con.createStatement();
            String jenis;
            int harga;
            String pilih;
            if (standar.isSelected()) {
                jenis="standar";
                harga = 300000;

            }else if(dobel.isSelected()){
                jenis="doubel";
                harga = 500000;
            }else {
                jenis="superrior";
                harga = 700000;
            } 
             try{
                  Integer.parseInt(NomorKamar.getText());
                  if(NomorKamar.getText().length()!=3){
                      JOptionPane.showMessageDialog(null, "digit kamar tidak sesuai !!!");
                      return;
                  }
               }catch(Exception E){
                   JOptionPane.showMessageDialog(null, "Nomor kamar tidak valid tidak valid !!!!!");
               }
        

            stm.executeUpdate("insert into room values('"+NomorKamar.getText()+"','"+jenis+"','kosong','"+harga+"')");
            JOptionPane.showMessageDialog(null,"kamar sudah ditambahkan");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "nomor kamar sudah tidak dapat pakai lagi");
        }
