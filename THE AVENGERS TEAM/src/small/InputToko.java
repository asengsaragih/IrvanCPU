    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
        
        int blok = Integer.parseInt(txtblok.getText());
        String lantai = (String) cblantai.getSelectedItem();
        String ukuran = txtukuran.getText();
        String status = txtstatus.getText();
        String harga = txtharga.getText();
        
        


        try {
            Statement tsql = db.createStatement();
            tsql.execute("INSERT INTO toko(kode_toko,lantai,ukuran,status_toko,harga) VALUES("
                    +blok+",'"+lantai+"','"+ukuran+"','"+status+"','"+harga+"')");
            JOptionPane.showMessageDialog(this,"Data berhasil ditambahkan");
        
            clearForm();
            tableTampilData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Maaf, gagal menambahkan data");
        }
                             
    }//GEN-LAST:event_btntambahActionPerformed
private void clearForm(){
        txtblok.setText("");
        cblantai.setSelectedItem("");
        txtstatus.setText("");
        txtukuran.setText("");
        txtharga.setText("");
}
