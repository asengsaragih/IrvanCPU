    private void tbltokoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbltokoMouseClicked
        // TODO add your handling code here:
        int baris = tbltoko.rowAtPoint(evt.getPoint());
        
        String kode_toko = tbltoko.getValueAt(baris, 0).toString();
        txtblok.setText(kode_toko);
        
        String lantai = tbltoko.getValueAt(baris, 1).toString();
        cblantai.setSelectedItem(lantai);
        
        String ukuran = tbltoko.getValueAt(baris, 3).toString();
        txtukuran.setText(ukuran);
        
        String status = tbltoko.getValueAt(baris, 2).toString();
        txtstatus.setText(status);
        
        String harga = tbltoko.getValueAt(baris, 4).toString();
        txtharga.setText(harga);
    }//GEN-LAST:event_tbltokoMouseClicked

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        try {
            Statement tsql = db.createStatement();
            tsql.execute("UPDATE toko SET "
                    +" lantai = '"+cblantai.getSelectedItem()+
                    "', harga = '"+txtharga.getText()+
                    "', ukuran = '"+txtukuran.getText()+
                    "', status_toko = '"+txtstatus.getText()+
                     "' "+"WHERE kode_toko='"+txtblok.getText()+"'");
            JOptionPane.showMessageDialog(this,"Data berhasil diupdate");
        
            //clearForm();
            tableTampilData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }          
    }//GEN-LAST:event_btnupdateActionPerformed

