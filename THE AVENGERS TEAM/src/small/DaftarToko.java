    private void tbltokoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbltokoMouseClicked
        // TODO add your handling code here:
           // TODO add your handling code here:
        int baris = tbltoko.rowAtPoint(evt.getPoint());
        String id_pemilik = tbltoko.getValueAt(baris, 0).toString();
        txtidpemilik.setText(id_pemilik);
        
        String nama_pemilik = tbltoko.getValueAt(baris, 1).toString();
        txtnamapemilik.setText(nama_pemilik);
        
        String tempat_lahir = tbltoko.getValueAt(baris, 2).toString();
        txttempatlahir.setText(tempat_lahir);
        
        String tgl_lahir = tbltoko.getValueAt(baris, 3).toString();
        cbtgl.setSelectedItem(tgl_lahir);
        
         String bln_lahir = tbltoko.getValueAt(baris, 4).toString();
        cbbln.setSelectedItem(bln_lahir);
        
        String thn_lahir = tbltoko.getValueAt(baris, 5).toString();
        txtthn.setText(thn_lahir);
        
        String nohp = tbltoko.getValueAt(baris, 6).toString();
        txtnohp.setText(nohp);
        
        String alamat = tbltoko.getValueAt(baris, 7).toString();
        txtalamat.setText(alamat);
        
        String new_jenis_kelamin = "";
        String jenis_kelamin = tbltoko.getValueAt(baris, 8).toString();
        if (rblakilaki.isSelected()) {
            new_jenis_kelamin = "laki - laki";
        }else{
            new_jenis_kelamin = "perempuan";
        }
        
        
        String email = tbltoko.getValueAt(baris, 9).toString();
        txtemail.setText(email);
        
         String nama_toko = tbltoko.getValueAt(baris, 10).toString();
        txtnamatoko.setText(nama_toko);
        
         String jenis_barang = tbltoko.getValueAt(baris, 11).toString();
        txtjenisbarang.setText(jenis_barang);
    }//GEN-LAST:event_tbltokoMouseClicked

