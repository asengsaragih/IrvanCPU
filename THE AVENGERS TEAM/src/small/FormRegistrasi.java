        // TODO add your handling code here:
        
        int id_pemilik = Integer.parseInt(txtidpemilik.getText());
        String nama_pemilik = txtnamapemilik.getText();
        String tempat_lahir = txttempatlahir.getText();
        String bulan_lahir = (String) cbbln.getSelectedItem();
        String tgl_lahir = (String) cbtgl.getSelectedItem();
        String tahun_lahir = txtthn.getText();
        String nohp = txtnohp.getText();
        String alamat = txtalamat.getText();
        String jenis_kelamin = "";
        if (rblakilaki.isSelected()) {
            jenis_kelamin = "laki - laki";
        }else{
            jenis_kelamin = "perempuan";
        }
        String email = txtemail.getText();
        String nama_toko =  txtnamatoko.getText();
        String jenis_barang =  txtjenisbarang.getText();
        int jangka_waktu = Integer.parseInt(txttahun.getText());
        int lantai = (int) cbblok.getSelectedItem();
        String total = txttotal.getText();
        String tgl_masuk = txttglmasuk.getText();
        String tgl_keluar = txttglkeluar.getText();
        


        try {
            Statement tsql = db.createStatement();
            tsql.execute("INSERT INTO registrasi(id_pemilik, nama_pemilik,tempat_lahir,tgl_lahir,bln_lahir,thn_lahir,nohp, alamat, jenis_kelamin, email, nama_toko,"
                    + " jenis_barang, jangka_waktu,total,tgl_masuk,tgl_keluar, kode_toko) VALUES("+id_pemilik+",'"+nama_pemilik+"','"+tempat_lahir+"','"+tgl_lahir+"','"+bulan_lahir+"','"+tahun_lahir+"','"+nohp+"','"+alamat+"','"
                    +jenis_kelamin+"','"
                    +email+"','"
                    +nama_toko+"','"
                    +jenis_barang+"',"
                    +jangka_waktu+","
                    +total+",'"
                    +tgl_masuk+"','"
                    +tgl_keluar+"',"
                    +lantai+")");
            tsql.execute("update toko set status_toko='terisi' where kode_toko='"+lantai+"'");
            JOptionPane.showMessageDialog(this,"Data berhasil");
        
            clearForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
