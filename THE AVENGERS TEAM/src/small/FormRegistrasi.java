private void tampil_blok(){
    try {
        cbblok.removeAllItems();
        Statement tsql = db.createStatement();
         String lantai = (String) cblantai.getSelectedItem();
        String sql = "select kode_toko from toko where lantai='"+lantai+"' and status_toko =''";
        ResultSet res = tsql.executeQuery(sql);
        
        while(res.next()){
            Object[] ob = new Object[3];
            ob[0] = res.getString(1);
            
            cbblok.addItem(res.getInt(1));
            
        }
        res.close(); tsql.close();
    } catch (Exception e) {
    }
}
