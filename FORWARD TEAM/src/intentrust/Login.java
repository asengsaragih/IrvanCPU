    private void jPanel3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MousePressed
        // TODO add your handling code here:
        String usr = txt_username.getText();
        String pas = txt_password.getText();
        
        String queryLogin = "SELECT * FROM tbl_admin WHERE id_admin='" + usr + "' AND nm_admin='" + pas + "'";
        try {
            java.sql.Connection conn = (Connection) config.koneksiDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(queryLogin);
            java.sql.ResultSet rs = pst.executeQuery();
            rs.next();
            if (rs.getRow()>0) {
                SessionCookies.setIdAdm(rs.getString("id_admin"));
                SessionCookies.setNmAdm(rs.getString("nm_admin"));
                JOptionPane.showMessageDialog(this, "SELAMAT DATANG " + SessionCookies.getNmAdm());
                new MainMenu().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal Login");
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_jPanel3MousePressed

