    private void tableDataParkiran() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Jenis Kendaraan");
        model.addColumn("No. Polisi");
        model.addColumn("Masuk");
        model.addColumn("Keluar");
        
        try {
            Connection c= KoneksiDB.getKoneksi();
            Statement tsql = c.createStatement();
            ResultSet res = tsql.executeQuery("select k.jenis, d.nomor_polisi, d.masuk, d.keluar, k.id_jenis, d.id_jenis "
                    + "from parkir_data d, jenis_kendaraan k "
                    + "where d.id_jenis = k.id_jenis AND d.keluar IS NOT NULL "
                    + "order by d.id_jenis");
            while (res.next()) {
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
            } TblParkiran.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
    
